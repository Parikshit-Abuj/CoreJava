public class Mathoperations {
    static void main() {
        System.out.println(Math.PI);
        System.out.println(Math.E);

        double result;

        result = Math.pow(2,5);
        result = Math.abs(-5);
        result = Math.sqrt(9);
        result = Math.ceil(3.11);
        result = Math.floor(3.99);
        result = Math.max(10,20);
        result = Math.min(10,20);

        System.out.println(result);
    }
}
